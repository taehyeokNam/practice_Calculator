package calculator;

import java.util.*;

public class ArithmeticCalculator extends Calculator {

    @Override
    public double Add(double input1 , double input2) {
        return super.Add(input1, input2);
    }

    @Override
    public double Sub(double input1 , double input2) {
        return super.Sub(input1, input2);
    }

    @Override
    public double Multi(double input1 , double input2) {
        return super.Multi(input1, input2);
    }

    @Override
    public double Div(double input1 , double input2) {
        return super.Div(input1, input2);
    }

    @Override
    public void addResult(double value) {
        super.addResult(value);
    }
}
